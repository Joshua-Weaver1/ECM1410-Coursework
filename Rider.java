package cycling;

public class Rider {
    private String name;
    private int teamID;
    private int yearOfBirth;

    public void setName(String name) {
        this.name = name;
    }

    private void setTeamID(int teamID) {
        this.teamID = teamID;
    }

    public void setYearOfBirth(int yearOfBirth) {
        this.yearOfBirth = yearOfBirth;
    }
}